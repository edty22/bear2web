package bear2web.model;

public class PatternDetail {

	private String patternCd;

	private int seqId;

	private String marketCd;

	private int preDayCnt;

	private String patternAtype;


	public String getPatternCd() {
		return patternCd;
	}

	public void setPatternCd(String patternCd) {
		this.patternCd = patternCd;
	}

	public int getSeqId() {
		return seqId;
	}

	public void setSeqId(int seqId) {
		this.seqId = seqId;
	}

	public String getMarketCd() {
		return marketCd;
	}

	public void setMarketCd(String marketCd) {
		this.marketCd = marketCd;
	}

	public int getPreDayCnt() {
		return preDayCnt;
	}

	public void setPreDayCnt(int preDayCnt) {
		this.preDayCnt = preDayCnt;
	}

	public String getPatternAtype() {
		return patternAtype;
	}

	public void setPatternAtype(String patternAtype) {
		this.patternAtype = patternAtype;
	}





}
